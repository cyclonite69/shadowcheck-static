import React from 'react';
import { FilterPanel } from './FilterPanel';
import { ActiveFiltersSummary } from './ActiveFiltersSummary';

interface FilterPanelWrapperProps {
  isOpen: boolean;
  adaptedFilters: any;
}

export const FilterPanelWrapper: React.FC<FilterPanelWrapperProps> = ({
  isOpen,
  adaptedFilters,
}) => {
  if (!isOpen) return null;

  return (
    <div className="fixed top-16 left-3 w-72 bg-slate-900/95 backdrop-blur-xl rounded-xl border border-blue-500/20 shadow-2xl p-3 space-y-2 overflow-y-auto z-40 max-h-[calc(100vh-80px)] pointer-events-auto">
      <ActiveFiltersSummary adaptedFilters={adaptedFilters} compact />
      <FilterPanel density="compact" />
    </div>
  );
};
